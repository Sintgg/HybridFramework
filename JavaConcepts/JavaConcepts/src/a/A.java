package a;

public class A {
	
	private int a = 10;
	int b = 20;
	public int c = 30;
	protected int d = 40;
	
	public static void main(String[] args) {
		
		A obja = new A();
		System.out.println(obja.a);
		System.out.println(obja.b);
		System.out.println(obja.c);
		System.out.println(obja.d);
	}

}
