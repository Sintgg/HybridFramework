package operators;

public class ConcatOperator {

	public static void main(String[] args) {
		
		// + is the overloaded operator in java
		
		int a = 10;
		int b = 20;
		String c = "Hello";
		String d = "World";
		
		System.out.println(a+b);
		System.out.println(c+d);
		
		System.out.println("Sum of given numbers = "+(a+b));

	}

}
