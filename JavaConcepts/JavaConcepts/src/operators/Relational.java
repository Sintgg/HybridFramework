package operators;

public class Relational {

	public static void main(String[] args) {

		// < less than,<= less than or equal to,>,>=,==,!=

		int i = 100;
		int j = 101;

		System.out.println(i < j);
		System.out.println(i <= j); 
		
	    int a = 5;
	    int b = 5;
	    System.out.println(a > b);
	    System.out.println(a >= b);
	    
	    System.out.println(a == b);
	    System.out.println(a != b);

	}

}
