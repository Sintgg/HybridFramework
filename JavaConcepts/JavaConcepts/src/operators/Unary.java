package operators;

public class Unary {
	
	// increment or decrement operator
	
	public static void main(String[] args) {
		
		
		int a = 10;
		a++; // increment the given value by + 1
		System.out.println(a);
		
		int b = 100;
		b--; // decrement the given value by -1
		System.out.println(b);
		
		
	}

}
